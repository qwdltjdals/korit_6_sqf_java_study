package com.study.java_study.ch03_함수;

public class Function01 {

    int add(int a, int b) { // add = int a와 int b를 가지고 있음
        int result = a + b; // result = a + b를 계산함
        return result; // result라는 결과를 return(반환)함
    }

    int sub(int a, int b) {
        int result = a - b;
        return result;
    }
}
